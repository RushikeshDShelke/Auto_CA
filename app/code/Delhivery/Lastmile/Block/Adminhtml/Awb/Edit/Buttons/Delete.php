namespace Delhivery\Lastmile\Block\Adminhtml\Awb\Edit\Buttons;

class Delete extends \Delhivery\Lastmile\Block\Adminhtml\Awb\Edit\Buttons\Generic implements \Magento\Framework\View\Element\UiComponent\Control\ButtonProviderInterface
{
    /**
     * get button data
     *
     * @return array
     */
    public function getButtonData()
    {
        $data = [];
        if ($this->getAwbId()) {
            $data = [
                'label' => __('Delete Manage&#x20;AWB'),
                'class' => 'delete',
                'on_click' => 'deleteConfirm(\'' . __(
                    'Are you sure you want to do this?'
                ) . '\', \'' . $this->getDeleteUrl() . '\')',
                'sort_order' => 20,
            ];
        }
        return $data;
    }

    /**
     * @return string
     */
    public function getDeleteUrl()
    {
        return $this->getUrl('*/*/delete', ['awb_id' => $this->getAwbId()]);
    }
}
